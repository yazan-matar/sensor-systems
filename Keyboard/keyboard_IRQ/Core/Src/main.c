void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	 if(htim == &htim2){
		 for (int j = 0; j < ROW_NUM; j++) {
			  isPressed = !HAL_GPIO_ReadPin(ROWS[j].port, ROWS[j].pin);
			  if (isPressed) {
				  if (!longPress[j][i]) {
					  buflen = snprintf(buffer, sizeof(buffer), "%c", SYMBOLS[j][i]);
					  HAL_UART_Transmit(&huart2, buffer, buflen, 100);
					  longPress[j][i] = 1;
				  }
			  }
			  else
				  longPress[j][i] = 0;
		 }
		 HAL_GPIO_WritePin(COLS[i].port, COLS[i].pin, GPIO_PIN_RESET);
		 if(++i > COL_NUM - 1)
			 i = 0;
		 HAL_GPIO_WritePin(COLS[i].port, COLS[i].pin, GPIO_PIN_SET);

	 }
}
